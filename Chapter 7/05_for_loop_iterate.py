#for loop iterate with lists
l = [1, 4, 6, 234, 6, 764]
for i in l:
    print(i)  # Print each element in the list l

#for loop iterate with tuples
t = (6, 231, 75, 122)
for i in t:
    print(i)  # Print each element in the tuple t

#for loop iterate with strings
s = "Harry"
for i in s:
    print(i)  # Print each character in the string s